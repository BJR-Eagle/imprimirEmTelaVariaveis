public class arq1 {
    public static void main(String[] args) {

        //Declarar uma variavel int (inteira)
        int a=10;

        //Declarar uma variavel double (decimal)
        double b=10.5;

        //Declarar uma variavel string (texto)
        String c="Junior é incrivel!!!";

        //declarar uma variavel boolean (binaria(true/false))
        boolean d=true;

        //imprimir as variaveis na tela
        System.out.println("O Valor inteiro é: "+a);
        System.out.println("O Valor decimal é: "+b);
        System.out.println("O Valor String é: "+c);
        System.out.println("O boolean é: "+d);
    }
}
